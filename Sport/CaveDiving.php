  <h1 class="page-head">Cave Diving</h1>
  <h5>General:</h5>
  
  As their name suggests you have to dive in a cave. You dive up to <mark>40 metres</mark> and you have to navigate in darkness, so you need a good sense of direction.
  There are three zones you can dive. 
  <br>
  </div>
  <div class="container">
  <br>
  <h6>First Zone: Cavern</h5>
  
  In the first zone, you dive up to 20 metres. In this depth you still have natural light. There is a fix rope which leads you the way down.
  <br>
  <h6>Second Zone: Cave</h6>
  
  You dive without natural light up to 30 metres. You explore some small caves.
  <br>
  <h6>Third Zone: Full Cave</h6>
  
  This is the last Zone. There is no limit for the depth. Your expeditions can take several days.
  <br>
  <br>
  </div>
  <div class="container">
  <h5>Requirements:</h5>
  <ul class="list-group list-group-flush">
          <li class="list-group-item">Minimum age: 16</li>
          <li class="list-group-item">Diver license: Rescue Diver</li>
          <li class="list-group-item">Minimum 25 loged dives</li>
          <li class="list-group-item">Minimum 5 loged dives at night</li>
          <li class="list-group-item">Valid dive report</li>
  </ul>
  </div>
  <div class="container">
    <br>
  <h5>Risks: </h5>
          Cave diving is 130 times more dangerous than normal diving. Statistics show there is a link between the experience of a diver and accidents. 
          The highest risk is to panic in the dark.
  </div>
  <br>
